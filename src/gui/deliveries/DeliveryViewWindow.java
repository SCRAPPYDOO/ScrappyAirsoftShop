    private void addDeliveryMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addDeliveryMouseReleased
        String id = deliveryId.getText();
        if(id.equals("")) { return; }
        
        String sup = supplier.getText();
        if(sup.equals("")) { return; }
        
        String code = itemCode.getText();
        if(code.equals("")) { return; }
        
        String amount = itemAmount.getText();     
        if(amount.equals("")) { return; }
        
        BigDecimal price = new BigDecimal(itemPrice.getText());
          
        //Dodajemy delivery do deliveries
        Map<TableStructInterface, Object> values = new HashMap();
        values.put(DeliveriesStruct.DELIVERY_ID, id);
        values.put(DeliveriesStruct.SUPPLIER, sup);
        String sqlReplace = Sql.getReplaceIntoQuery(DeliveriesStruct.getTableName(), values);
        if(!SqlConnector.doInsertQuery(sqlReplace)) {
            return;
        }
        
        //Dodajemy itemy do orderu        
        values = new HashMap();
        values.put(ItemsInStruct.ITEM_ORDER_ID, id);
        values.put(ItemsInStruct.ITEM_CODE, code);
        values.put(ItemsInStruct.ITEM_AMOUNT, amount);
        values.put(ItemsInStruct.ITEM_PRICE, price);
        
        values.put(ItemsInStruct.ITEM_ACTUAL_AMOUNT, amount);
        values.put(ItemsInStruct.ITEM_SUM_PRICE, price.multiply( new BigDecimal(amount)));
        
        sqlReplace = Sql.getReplaceIntoQuery(ItemsInStruct.getTableName(), values);
        SqlConnector.doInsertQuery(sqlReplace);
        
        this.dispose();
        parent.setWindowState(true);
    }//GEN-LAST:event_addDeliveryMouseReleased

