    private void createTable() {
        String[] itemTableStruct;
        if(viewType == DeliveryViewWindowType.VIEW_DELIVERY) {
            itemTableStruct = ItemsInStruct.getSelectTableStruct();
        } else {
            itemTableStruct = ItemsInStruct.getInsertTableStruct();
        }

        DefaultTableModel model = new DefaultTableModel(null, itemTableStruct);
        
        itemsTable.setModel(model);
    }
